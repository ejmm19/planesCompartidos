	<legend>Guía Para Crear Planes de Clases</legend>
	
  <p><b>DATOS:</b></p>
  <p>Materia, Profesor a cargo, Tema y fecha de la clase</p>
  <br>
  <p><b>OBJETIVOS DE LA CLASE: </b></p>
  <p>Son enunciados cortos y simples, que expresan las competencias (conceptos, procedimientos, actitudes) 
  que usted pretende que el alumno logre durante la clase. Para formularlos adecuadamente, usted debe tener 
  en cuenta los siguientes requisitos: Cada objetivo debe estar expresado en una única oración. Su formulación
   debe ser clara, concisa y breve. El enunciado debe comenzar con un verbo en infinitivo. Deben referirse a
    capacidades intelectuales, prácticas y sociales.</p><br>

    <p><b>CONTENIDOS QUE SE ABORDARÁN EN LA CLASE: </b></p>
  <p>Los contenidos son los aprendizajes que los alumnos deben adquirir. Estos aprendizajes deberán ser integrales; 
  es decir que deberán abarcar, al menos, tres dimensiones: lo conceptual, lo procedimental y lo actitudinal.</p>
  <br>
    <p><b>CONTENIDOS CONCEPTUALES:</b></p>
  <p>Son objetos de conocimiento referidos a datos, hechos, conceptos y principios. La actividad que requieren 
  por parte del alumno es esencialmente intelectual.</p>
  <br>
    <p><b>CONTENIDOS PROCEDIMENTALES:</b></p>
  <p>El objeto a conocer es una destreza, una habilidad o una estrategia. Para aprenderlos, el alumno deberá 
  disponerse a hacer algo, a realizar un procedimiento.</p>
  <br>
    <p><b>CONTENIDOS ACTITUDINALES:</b></p>
  <p>El objeto a incorporar está constituido por actitudes, valores y normas. Hacen referencia a los aprendizajes
   vinculados a las dimensiones ética y social.</p>
   <br>
    <p><b>ESTRATEGIAS DIDÁCTICAS:</b></p>
  <p>Las estrategias didácticas se redactan para dar cuenta de la manera en que el docente piensa plantear la clase. 
  Es el diseño de las acciones que el docente realizará con el fin de que los alumnos aprendan. Se redactan de manera breve,
   clara, concisa.</p>
   <br>
    <p><b>ACTIVIDADES:</b></p>
  <p>Son las acciones que los alumnos deberán realizar durante la clase con el fin de aprender los contenidos propuestos.</p>
   <br>
    <p><b>RECURSOS:</b></p>
  <p>Es el listado de materiales que se precisarán para la implementación de las estrategias didácticas y las actividades programadas.</p>
  <br>
    <p><b>ESTIMACIÓN DEL TIEMPO:</b></p>
  <p>Es la cantidad de horas cátedras que durará la clase.</p>
  <br>
    <p><b>EVALUACIÓN</b></p>
  <p>Si durante la clase, se implementará algún tipo de evaluación, deberá dejarse constancia de la misma en el Plan de Clases.</p>
